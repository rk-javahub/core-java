package com.rkjavahub.OperatorAndAssignment.Demo;

public class ArithmeticOperator {

	public static void main(String[] args) {
		/*
		 * Whenever we are performing any arithmetic operation between two
		 * variables a and b the result type is always max(int,type of a,type of
		 * b) 
		 * byte b=10;
		 * b++;
		 * System.out.println(b);
		 * b=b+1; // Type mismatch: cannot convert from int to byte
		 * System.out.println(b);
		 * 
		 * System.out.println(10 + 0.0); // 10.0 
		 * System.out.println('a' + 'b'); //195 
		 * System.out.println(100 + 'a'); // 197
		 */

		/*
		 * Infinity
		 * 
		 * System.out.println(10 / 0); // java.lang.ArithmeticException: / by
		 * zero
		 * 
		 * System.out.println(10.0 /0); //Infinity
		 * 
		 * System.out.println(0.0 / 0); // NaN
		 * System.out.println(Math.sqrt(4));// 2.0
		 * System.out.println(Math.sqrt(-4));// NaN
		 */

	}

}
